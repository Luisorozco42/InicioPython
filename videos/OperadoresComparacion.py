# Estos también se conocen como operadores racionales
print("*** Operadores de comparación (Relacionales) ***")
a, b = 7, 5
print(f"Valor inicial: {a}, b: {b}")

# Operador de igualdad
resultado = a == b
print(f"Resultado de evaluar a == b es: {resultado}")

# Operador diferente
resultado = a != b
print(f"Resultado de evaluar a != b es: {resultado}")

# Operador de mayor que
resultado = a > b
print(f"Resultado de evaluar a > b es: {resultado}")

# Operador de mayor igual que
resultado = a >= b
print(f"Resultado de evaluar a >= b es: {resultado}")

# Operador de menor que
resultado = a < b
print(f"Resultado de evaluar a < b es: {resultado}")

# Operador menor igual que
resultado = a <= b
print(f"Resultado de evaluar a <= b es: {resultado}")