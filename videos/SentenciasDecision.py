print("*** Sentencia If ***")

edad = 16

if edad >= 18:
    print(f"Eres mayor de edad. Tienes {edad} años")
elif 13 <= edad < 18:
    print(f"Eres un adolescente. Tienes {edad} años")
else:
    print(f"Eres menor de edad. Tienes {edad} años")
