# Vamos a ver primeramente los operadores matemáticos para realizar código

# Operadores aritméticos
a = 10
b = 3

# Suma
suma = a + b
print(f"Suma: {suma}")

# Resta
resta = a - b
print(f"Resta: {resta}")

# Multiplication
multiplicacion = a * b
print(f"Multiplicación: {multiplicacion}")

# División entera
division = a / b
print(f"División: {division}")

# Division entera, donde queda residuo
division_entera = a // b
print(f"División entera: {division_entera}")

# Módulo % residuo de la división
modulo = a % b
print(f"Residuo de la división: {modulo}")

# Exopente o potencia
exponente = a ** b
print(f"Exponente: {exponente}")
