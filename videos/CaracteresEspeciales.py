# Caracteres especiales en python

# Salto de linea
mensaje = "Hola\nMundo"
print(mensaje)

# Tabulacion

mensaje2 = "\tJuan Perez"
print(mensaje2)

#Cadena dentro de comillas
mensaje3 = "Ella dijo \"Hola a todos\""
print(mensaje3)

#Barra invertida
mensaje4 = "C:\\Usuarios\\Marce\\Documentos"
print(mensaje4)

#cadenas crudas
mensaje5 = r"C:\Usuarios\Marce\Documentos"
print(mensaje5)
