# En este reto toca calcular el área de un rectángulo y su perímetro
# Área = base * altura
# Perimetro = 2 * (base + altura)

print("*** sistema de Cálculos del Area y Perímetro de un Rectángulo ***")
base = float(input("Ingrese el valor de la base: "))
altura = float(input("Ingrese el valor de la altura: "))

area = base * altura
perimetro = 2 * (base + altura)

print(f"El area del rectanngulo es de: {area} y  el perimetro es de: {perimetro}")
